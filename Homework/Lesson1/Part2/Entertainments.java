public class Entertainments {
	private String type;
	private String place;
	private String skills;
	private String physicalHealth;
	private String educability;
	private String mood;
	private String memory;
	private String imagination;
	private boolean wish;
	private boolean fears;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getPlace() {
		return place;
	}

	public void setPlace(String place) {
		this.place = place;
	}

	public String getSkills() {
		return skills;
	}

	public void setSkills(String skills) {
		this.skills = skills;
	}

	public String getPhysicalHealth() {
		return physicalHealth;
	}

	public void setPhysicalHealth(String physicalHealth) {
		this.physicalHealth = physicalHealth;
	}

	public String getEducability() {
		return educability;
	}

	public void setEducability(String educability) {
		this.educability = educability;
	}

	public String getMood() {
		return mood;
	}

	public void setMood(String mood) {
		this.mood = mood;
	}

	public String getMemory() {
		return memory;
	}

	public void setMemory(String memory) {
		this.memory = memory;
	}

	public String getImagination() {
		return imagination;
	}

	public void setImagination(String imagination) {
		this.imagination = imagination;
	}

	public boolean isWish() {
		return wish;
	}

	public void setWish(boolean wish) {
		this.wish = wish;
	}

	public boolean isFears() {
		return fears;
	}

	public void setFears(boolean fears) {
		this.fears = fears;
	}
}